public class Main {
    public static void main(String... args) {
        for (char i = 'A'; i <= 'Z'; i++) {
            int n = i;
            System.out.println(i + " : " + n);
        }
        for (char i = 'a'; i <= 'z'; i++) {
            int k = i;
            System.out.println(i + " : " + k);
        }
    }
}

